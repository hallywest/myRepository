package com.hallywest.charactor;

public class Charactor {

	private int level;
	private String name;
	private double hp;
	private double mp;
	private double pow; // power
	private double spt; // spirit
	private double sta; // stamina
	private double act; // action

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getHp() {
		return hp;
	}

	public void setHp(double hp) {
		this.hp = hp;
	}

	public double getMp() {
		return mp;
	}

	public void setMp(double mp) {
		this.mp = mp;
	}

	public double getPow() {
		return pow;
	}

	public void setPow(double pow) {
		this.pow = pow;
	}

	public double getSpt() {
		return spt;
	}

	public void setSpt(double spt) {
		this.spt = spt;
	}

	public double getSta() {
		return sta;
	}

	public void setSta(double sta) {
		this.sta = sta;
	}

	public double getAct() {
		return act;
	}

	public void setAct(double act) {
		this.act = act;
	}
}
